function ___generateTable() {
  const voltbl = [];
  for (let i = 0; i < 16; i++) {
    voltbl[i] = 256 / Math.pow(2, i / 2);
  }
  const pcmtbl = [];

  for (let i = 0; i < 16; i++) {
    for (let j = 0; j < 16; j++) {
      for (let k = 0; k < 16; k++) {
        const v = voltbl[i] + voltbl[j] + voltbl[k];
        if (pcmtbl[v] == null) {
          pcmtbl[v] = [i, j, k];
        }
      }
    }
  }

  let last = [0, 0, 0];
  for (let i = 768; i >= 0; i--) {
    /** down to 7-bit reso. this decreases noise. */
    if (pcmtbl[i] != null) {
      console.log(`${i}: [${pcmtbl[i]}],`);
      last = pcmtbl[i];
    } else {
      console.log(`${i}: [${last}],`);
    }
  }
}
